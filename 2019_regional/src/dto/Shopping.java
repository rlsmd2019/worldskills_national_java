package dto;

public class Shopping {
	public int no;
	public int menuNo;
	public int price;
	public int count;
	public String size;
	public int amount;
	public String menuName;
	public String group;
	
	public Shopping(int no, int menuNo, int price, int count, String size, int amount, String menuName, String group) {
		super();
		this.no = no;
		this.menuNo = menuNo;
		this.price = price;
		this.count = count;
		this.size = size;
		this.amount = amount;
		this.menuName = menuName;
		this.group = group;
	}
	
	
	
}
